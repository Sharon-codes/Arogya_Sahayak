export function t(key: string, language: string = 'en'): string {
  const keys = key.split('.');
  let value: any = translations[language as keyof typeof translations] || translations.en;
  
  for (const k of keys) {
    if (value === undefined) {
      // Fallback to English if key not found in selected language
      let enValue: any = translations.en;
      for (const enK of keys) {
        enValue = enValue?.[enK];
      }
      return enValue || key;
    }
    value = value?.[k];
  }
  
  return value || key;
}
